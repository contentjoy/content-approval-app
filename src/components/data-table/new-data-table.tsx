export const schema = z.object({
  id: z.string(),
  gymId: z.string(),
  gymName: z.string(),
  status: z.enum(["Active", "Off-Boarded"]),
  gymSlug: z.string(),
  createdAt: z.string(),
  lastUploadDate: z.string().nullable(),
  lastDeliveryDate: z.string().nullable(),
  lastScheduleDate: z.string().nullable(),
  lastPostScheduled: z.string().nullable(),
  socials: z.array(z.object({
    platform: z.enum(['facebook', 'instagram', 'tiktok', 'youtube']),
    connected_at: z.string().optional(),
    profile_key: z.string().optional(),
    platform_username: z.string().optional(),
    ayrshare_profiles: z.union([z.string(), z.record(z.string(), z.object({
      profile_key: z.string().optional(),
      connected_at: z.string().optional()
    }))]).optional()
  })),
  deliveredMTD: z.number(),
  approvedMTD: z.number(),
  approvalRatePct: z.number(),
  uploadsMTD: z.number(),
  scheduledMTD: z.number()
})

// Create a separate component for the drag handle
function DragHandle({ id }: { id: string }) {
  const { attributes, listeners } = useSortable({
    id,
  })

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="text-muted-foreground size-7 hover:bg-transparent"
    >
      <IconGripVertical className="text-muted-foreground size-3" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  )
}

